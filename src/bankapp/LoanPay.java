    private void ButtonCheckActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonCheckActionPerformed

        if (tfAccount.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Account Number");
        } else {
            account = Integer.parseInt(tfAccount.getText());

            Connection con = null;
            PreparedStatement ps = null;
            ResultSet rs = null;

            try {
                con = DatabaseConnection.getConnection();
                ps = con.prepareStatement("select account_number from loan where account_number=?");
                ps.setInt(1, account);
                rs = ps.executeQuery();
                if (rs.next()) {
                    loanCheck();
                } else {
                    JOptionPane.showMessageDialog(this, "No Record Found.");
                }
            } catch (Exception e) {
                e.printStackTrace();
            } finally {
                try {
                    con.close();
                    ps.close();
                } catch (SQLException ex) {
                    System.out.println(ex);
                }
            }
        }
    }//GEN-LAST:event_ButtonCheckActionPerformed

    private void loanCheck() {
        Connection con = null;
        Statement stm = null;
        ResultSet rs = null;
        try {
            con = DatabaseConnection.getConnection();
            stm = con.createStatement();
            rs = stm.executeQuery("select month from loan where account_number = '" + account + "' ");
            while (rs.next()) {
                month = rs.getDouble("month");
            }
            if (month > 0) {
                loanBalSet();
            } else {
                JOptionPane.showMessageDialog(this, "Currently You Have No Loan");
            }
        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            try {
                con.close();
                stm.close();
            } catch (SQLException ex) {
                System.out.println(ex);
            }
        }
    }

    private void loanBalSet() {
        Connection con = null;
        Statement stm = null;
        ResultSet rs = null;
        try {
            con = DatabaseConnection.getConnection();
            stm = con.createStatement();
            rs = stm.executeQuery("select name, father_name, mobile_number, loan, payable, month,"
                    + " payment from loan_managment natural join loan"
                    + " where account_number = '" + account + "' ");
            while (rs.next()) {
                name = rs.getString("name");
                father = rs.getString("father_name");
                mobile = rs.getString("mobile_number");
                loan = rs.getDouble("loan");
                payable = rs.getDouble("payable");
                month = rs.getDouble("month");
                payment = rs.getDouble("payment");

                tfName.setText(name);
                tfFather.setText(father);
                tfMobile.setText(mobile);
                tfLoan.setText(Double.toString(loan));
                tfDueLoan.setText(Double.toString(payable));
                tfDueMonth.setText(Double.toString(month));
                tfPayment.setText(Double.toString(payment));
            }
        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            try {
                con.close();
                stm.close();
            } catch (SQLException ex) {
                System.out.println(ex);
            }
        }

    }
