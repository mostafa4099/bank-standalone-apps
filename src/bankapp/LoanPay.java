    private void validCalc() {
        if (tfDueLoan.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please Check Account Number First", "Loan System", JOptionPane.INFORMATION_MESSAGE);
        } else if (tfCMonth.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please Input Number Of Pay Month", "Loan System", JOptionPane.INFORMATION_MESSAGE);
        } else {
            calculate();
        }
    }

    private void calculate() {
        loan = Double.parseDouble(tfLoan.getText());
        payable = Double.parseDouble(tfDueLoan.getText());
        month = Double.parseDouble(tfDueMonth.getText());
        payment = Double.parseDouble(tfPayment.getText());
        cMonth = Double.parseDouble(tfCMonth.getText());

        cPayment = payment * cMonth;
        dLoan = payable - cPayment;
        dMonth = month - cMonth;

        tfCPayment.setText(String.valueOf(cPayment));
    }

