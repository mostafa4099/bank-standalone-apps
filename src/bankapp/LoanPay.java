    private void paymentLoan() {

        Connection con = null;
        Statement stm = null;
        try {
            con = DatabaseConnection.getConnection();
            stm = con.createStatement();
            int ctr = 0;
            ctr = stm.executeUpdate("update loan set payable='" + dLoan + "', month='" + dMonth + "', cmonth='"+cMonth+"', cpayment='"+cPayment+"' ");
            if (ctr > 0) {
                JOptionPane.showMessageDialog(this, "Payment Complated for " + cMonth + " month");

                tfCDueLoan.setText(String.valueOf(dLoan));
                tfCDueMonth.setText(String.valueOf(dMonth));
            } else {
                JOptionPane.showMessageDialog(this, "Payment failed", "Error", JOptionPane.ERROR_MESSAGE);
            }

        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            try {
                con.close();
                stm.close();
            } catch (SQLException ex) {
                System.out.println(ex);
            }
        }

    }


