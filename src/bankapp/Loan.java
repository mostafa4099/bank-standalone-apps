    private void loanBalSet() {
        Connection con = null;
        Statement stm = null;
        ResultSet rs = null;
        try {
            con = DatabaseConnection.getConnection();
            stm = con.createStatement();
            rs = stm.executeQuery("select name, father_name, mother_name, mobile_number, loan, interest, month,"
                    + " payment, payable from loan_managment natural join loan"
                    + " where account_number = '" + account + "' ");
            while (rs.next()) {
                name = rs.getString("name");
                father = rs.getString("father_name");
                mother = rs.getString("mother_name");
                mobile = rs.getString("mobile_number");
                loan = rs.getDouble("loan");
                interest = rs.getDouble("interest");
                month = rs.getDouble("month");
                payment = rs.getDouble("payment");
                payable = rs.getDouble("payable");

                tfName.setText(name);
                tfFather.setText(father);
                tfMother.setText(mother);
                tfMobile.setText(mobile);
                tfLoan.setText(Double.toString(loan));
                tfInterest.setText(Double.toString(interest));
                tfMonth.setText(Double.toString(month));
                tfPayment.setText(Double.toString(payment));
                tfPayable.setText(Double.toString(payable));
            }
        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            try {
                con.close();
                stm.close();
            } catch (SQLException ex) {
                System.out.println(ex);
            }
        }
    }

