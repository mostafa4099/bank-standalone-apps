    private void validCalc() {
        if (tfLoan.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please Input Loan Amount", "Loan System", JOptionPane.INFORMATION_MESSAGE);
        } else if (tfInterest.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please Input Interest Rate", "Loan System", JOptionPane.INFORMATION_MESSAGE);
        } else if (tfMonth.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please Input Number Of Payment", "Loan System", JOptionPane.INFORMATION_MESSAGE);
        } else {
            calculate();
        }
    }

    private void calculate() {
        loan = Double.parseDouble(tfLoan.getText());
        interest = Double.parseDouble(tfInterest.getText());
        month = Double.parseDouble(tfMonth.getText());

        payable = loan + (loan * interest) / 100;
        payment = payable / month;

        tfPayment.setText(String.valueOf(payment));
        tfPayable.setText(String.valueOf(payable));
    }
