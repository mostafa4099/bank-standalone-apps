    private void validAndSave() {
        if (tfName.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please Check Account Number First", "Bank System", JOptionPane.INFORMATION_MESSAGE);
        } else if (tfDeposit.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please Input Minimum Ammount", "Bank System", JOptionPane.INFORMATION_MESSAGE);
        } else if (dateChooser.getDate() == null) {
            JOptionPane.showMessageDialog(null, "Please Input Date", "Bank System", JOptionPane.INFORMATION_MESSAGE);
        } else {
            saveData();
        }
    }

    private void saveData() {
        getValues();

        Connection con = null;
        Statement stm = null;
        try {
            con = DatabaseConnection.getConnection();
            stm = con.createStatement();
            int ctr = 0;
            ctr = stm.executeUpdate("update loan_managment set deposit='" + deposit + "', balance='" + balance + "', date='" + date + "'"
                    + " where account_number='" + account + "'");
            if (ctr > 0) {
                JOptionPane.showMessageDialog(this, "Deposited");
            } else {
                JOptionPane.showMessageDialog(this, "Deposit failed", "Error", JOptionPane.ERROR_MESSAGE);
            }

        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            try {
                con.close();
                stm.close();
            } catch (SQLException ex) {
                System.out.println(ex);
            }
        }
    }

    private void getValues() {
        deposit = Double.valueOf(tfDeposit.getText());
        balance = balance + deposit;
        date = sdf.format(dateChooser.getDate());
    }
